    private void report_collections(final Srpt_cash_count to, String jrxml_name) {
        jPanel2.removeAll();
        jPanel2.setLayout(new BorderLayout());
        try {
            JRViewer viewer = get_viewer_collections(to, jrxml_name);
            JPanel pnl = new JPanel();
            pnl.add(viewer);
            pnl.setVisible(true);
            pnl.setVisible(true);
            jPanel2.add(viewer);
            jPanel2.updateUI();
        } catch (Exception e) {
            throw new RuntimeException(e);
        }
    }

    public static JRViewer get_viewer_collections(Srpt_cash_count to, String rpt_name) {
        try {
            return JasperUtil.getJasperViewer(
                    compileJasper_collections(rpt_name),
                    JasperUtil.setParameter(to),
                    JasperUtil.emptyDatasource());
        } catch (Exception e) {
            throw new RuntimeException(e);
        } finally {
        }
    }

    public static JasperReport compileJasper_collections(String rpt_name) {
        try {
            String jrxml = rpt_name;
            InputStream is = Srpt_cash_count.class.getResourceAsStream(jrxml);
            JasperReport jasper = JasperCompileManager.compileReport(is);
            return jasper;
        } catch (JRException e) {
            throw new RuntimeException(e);
        }
    }

