    public void do_pass(final String business_name,final String address,final String operated_by,final double cash_in
            ,final double thousand,final double five_hundred,final double two_hundred,final double one_hundred, final double fifty,final double twenty
            ,final double coins,final double cash_total,final String cashier_name,final double net_total,final String date
            ,final List<Srpt_checks.field> checks,final List<Srpt_checks.field> expenses,final double grand_sales,final double grand_checks,final double grand_expenses
    ) {
        jProgressBar1.setString("Loading...Please wait...");
        jProgressBar1.setIndeterminate(true);

        Thread t = new Thread(new Runnable() {
            @Override
            public void run() {
               
                String SUBREPORT_DIR = System.getProperty("img_path", "C:\\Users\\Guinness\\") + "retail_res\\rpt\\";
                Srpt_cash_count rpt = new Srpt_cash_count(business_name, address, operated_by, cash_in, thousand, five_hundred, two_hundred
                        , one_hundred, fifty, twenty, coins, cash_total, cashier_name, net_total, date, checks, SUBREPORT_DIR
                        , expenses, grand_sales, grand_checks, grand_expenses);
               
                String jrxml = "rpt_cash_count.jrxml";
                report_collections(rpt, jrxml);
                try {
                    JasperReport jasperReport = null;
                    InputStream is = Srpt_cash_count.class.getResourceAsStream(jrxml);
                    jasperReport = JasperCompileManager.compileReport(is);
                    jasperPrint = JasperFillManager.fillReport(jasperReport, JasperUtil.
                            setParameter(rpt), JasperUtil.emptyDatasource());
                } catch (JRException ex) {
                    Logger.getLogger(Srpt_void_items.class.getName()).
                            log(Level.SEVERE, null, ex);
                }

                jProgressBar1.setString("Finished...");
                jProgressBar1.setIndeterminate(false);
            }
        });

        t.start();
    }

