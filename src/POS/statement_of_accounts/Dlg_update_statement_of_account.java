    private void ok() {
        double qty = FitIn.toDouble(jTextField1.getText());
        if (qty <= 0) {
            Alert.set(0, "Enter Qty");
            return;
        }
        if (callback != null) {
            callback.ok(new CloseDialog(this), new OutputData(qty));
        }
    }
