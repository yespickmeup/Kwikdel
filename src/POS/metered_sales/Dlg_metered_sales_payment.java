    private void select() {
        if (!jCheckBox1.isSelected()) {
            jTextField4.grabFocus();
            jTextField3.setText(jTextField1.getText());
            jTextField2.setText("");
        } else {
            jTextField2.setText(jTextField1.getText());
            jTextField3.setText("");
        }
    }

    private void ok() {
        String or_no = jTextField6.getText();
        double cash = 0;
        double check_amount = 0;
        String check_bank = "";
        String check_no = "";
        if (jCheckBox1.isSelected()) {
            cash = FitIn.toDouble(jTextField1.getText());
        } else {
            check_amount = FitIn.toDouble(jTextField1.getText());
            check_bank = jTextField4.getText();
            check_no = jTextField5.getText();
        }

        if (callback != null) {
            callback.ok(new CloseDialog(this), new OutputData(or_no, cash, check_amount, check_bank, check_no));
        }
    }
