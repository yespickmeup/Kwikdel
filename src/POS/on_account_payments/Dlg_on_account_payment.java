    private void select_check() {
        if (jCheckBox1.isSelected()) {
            jTextField2.setFocusable(false);

            jTextField3.setFocusable(true);
            jTextField4.setFocusable(true);
            jTextField2.grabFocus();
            jTextField1.setFocusable(false);

            jTextField2.setText(jLabel8.getText());
            jTextField1.setText("0.00");

        } else {

            jTextField2.setFocusable(false);
            jTextField3.setFocusable(false);
            jTextField4.setFocusable(false);
            jTextField1.setFocusable(false);
            jTextField1.setText(jLabel8.getText());
            jTextField2.setText("0.00");
            jTextField1.grabFocus();
        }
    }

    List<S1_banks.to_banks> driver_list = new ArrayList();

    private void init_driver() {
        String search = jTextField3.getText();
        driver_list.clear();
        driver_list = S1_banks.ret_data("");
        Object[][] obj = new Object[driver_list.size()][1];
        int i = 0;
        for (S1_banks.to_banks to : driver_list) {
            obj[i][0] = to.bank_name;
            i++;
        }
        JLabel[] labels = {};
        int[] tbl_widths_customers = {500};
        int width = 0;
        String[] col_names = {""};
        TableRenderer tr = new TableRenderer();
        TableRenderer.setPopup(jTextField3, obj, labels, tbl_widths_customers, col_names);
        tr.setCallback(new TableRenderer.Callback() {
            @Override
            public void ok(TableRenderer.OutputData data) {
                S1_banks.to_banks to = driver_list.get(data.selected_row);
                jTextField3.setText(to.bank_name);

            }
        });
    }

    private void ok() {
        double check_amount = 0;
        String check_bank = "";
        String check_no = "";
        if (jCheckBox1.isSelected()) {
            check_amount = FitIn.toDouble(jLabel2.getText());
            check_bank = jTextField3.getText();
            check_no = jTextField4.getText();
        }
        if (callback != null) {
            callback.ok(new CloseDialog(this), new OutputData(check_amount, check_bank, check_no));
        }
    }
