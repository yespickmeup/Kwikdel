    private void ok() {
        double qty = FitIn.toDouble(jTextField1.getText());
        if (qty <= 0) {
            qty = 1;
        }
        double cost = FitIn.toDouble(jTextField2.getText());
        if (callback != null) {
            callback.ok(new CloseDialog(this), new OutputData(qty, cost));
        }
    }
