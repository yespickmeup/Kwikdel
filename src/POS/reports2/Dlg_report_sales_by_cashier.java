    private void get_data() {
        jProgressBar1.setString("Loading...Please wait...");
        jProgressBar1.setIndeterminate(true);
        Thread t = new Thread(new Runnable() {
            @Override
            public void run() {
                String date_from = DateType.sf.format(dp_from.getDate());
                String date_to = DateType.sf.format(dp_to.getDate());
                String where = "";
                String where2 = "";
                String where3 = "";
                if (!jCheckBox1.isSelected()) {
                    where = " where user_name='" + jTextField2.getText() + "' and Date(date_added) between '" + date_from + "' and '" + date_to + "' order by user_name,payment_type";
                    where2 = " where user_name='" + jTextField2.getText() + "' and Date(date_added) between '" + date_from + "' and '" + date_to + "' group by barcode,selling_price order by description asc ";
                    where3 = " where user_name='" + jTextField2.getText() + "' and Date(date_added) between '" + date_from + "' and '" + date_to + "' group by barcode,selling_price order by is_deposit,description asc ";
                } else {
                    where = " where Date(date_added) between '" + date_from + "' and '" + date_to + "' order by user_name,payment_type";
                    where2 = " where Date(date_added) between '" + date_from + "' and '" + date_to + "' group by barcode,selling_price order by description asc ";
                    where3 = " where Date(date_added) between '" + date_from + "' and '" + date_to + "' group by barcode,selling_price order by is_deposit,description asc ";
                }

                double walkin_sales = 0;
                double delivery_sales = 0;
                List<Srpt_sales_transaction_by_cashier.field> transactions = Srpt_sales_transaction_by_cashier.ret_data(where);
                for (Srpt_sales_transaction_by_cashier.field f : transactions) {
                    if (f.selling_type.equalsIgnoreCase("Walkin")) {
                        walkin_sales += f.total;
                    }
                    if (f.selling_type.equalsIgnoreCase("Delivery")) {
                        delivery_sales += f.total;
                    }
                }
                List<Srpt_sales_items.field> items = ret_data(where2);

                String SUBREPORT_DIR = System.getProperty("img_path", "C:\\Users\\Ronald\\") + "retail_res\\rpt\\";
                String date = "Date: [" + date_from + "]-[" + date_to + "]";

                List<Srpt_deposits.field> deposits = Srpt_deposits.deposits(where3);
                List<Srpt_deposits.field> deposit_returns = Srpt_deposits.deposit_returns(where3);

                List<Srpt_deposits.field> rpt_deposits = new ArrayList();
                rpt_deposits.addAll(deposits);
                rpt_deposits.addAll(deposit_returns);

                Srpt_cashier_sales rpt = new Srpt_cashier_sales(SUBREPORT_DIR, transactions, items, date, rpt_deposits, walkin_sales, delivery_sales);
                String jrxml = "rpt_cashier_sales.jrxml";
                report_sales_items(rpt, jrxml);
                jButton2.setEnabled(true);
                try {
                    JasperReport jasperReport = null;
                    InputStream is = Srpt_sales_transaction_by_cashier.class.getResourceAsStream(jrxml);
                    jasperReport = JasperCompileManager.compileReport(is);
                    jasperPrint = JasperFillManager.fillReport(jasperReport, JasperUtil.
                            setParameter(rpt), JasperUtil.emptyDatasource());
                } catch (JRException ex) {
                    Logger.getLogger(Srpt_void_items.class.getName()).
                            log(Level.SEVERE, null, ex);
                }
                jProgressBar1.setString("Finished...");
                jProgressBar1.setIndeterminate(false);
            }
        });

        t.start();
    }

    private void report_sales_items(final Srpt_cashier_sales to, String jrxml_name) {
        pnl_sales_items.removeAll();
        pnl_sales_items.setLayout(new BorderLayout());
        try {
            JRViewer viewer = get_viewer_all_sales(to, jrxml_name);
            JPanel pnl = new JPanel();
            pnl.add(viewer);
            pnl.setVisible(true);
            pnl.setVisible(true);
            pnl_sales_items.add(viewer);
            pnl_sales_items.updateUI();
        } catch (Exception e) {
            throw new RuntimeException(e);
        }
    }

    public static JRViewer get_viewer_all_sales(Srpt_cashier_sales to, String rpt_name) {
        try {
            return JasperUtil.getJasperViewer(
                    compileJasper(rpt_name),
                    JasperUtil.setParameter(to),
                    JasperUtil.emptyDatasource());
        } catch (Exception e) {
            throw new RuntimeException(e);
        } finally {
        }
    }

    public static JasperReport compileJasper(String rpt_name) {
        try {
            String jrxml = rpt_name;
            InputStream is = Srpt_cashier_sales.class.getResourceAsStream(jrxml);
            JasperReport jasper = JasperCompileManager.compileReport(is);
            return jasper;
        } catch (JRException e) {
            throw new RuntimeException(e);
        }
    }

