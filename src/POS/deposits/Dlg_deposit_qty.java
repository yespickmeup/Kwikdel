    private void ok() {
        double qty = FitIn.toDouble(jTextField1.getText());
        int is_deposit = 0;
        if (jCheckBox1.isSelected()) {
            is_deposit = 1;
        }
        if (qty <= 0) {
            qty = 1;
        }
        if (callback != null) {
            callback.ok(new CloseDialog(this), new OutputData(qty, is_deposit));
        }
    }
