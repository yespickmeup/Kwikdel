    private void ok() {
        double qty = FitIn.toDouble(jTextField1.getText());
        String date = DateType.sf.format(jDateChooser1.getDate());
        if (qty <= 0 || qty > qtys) {
            qty = qtys;
        }
        if (callback != null) {
            callback.ok(new CloseDialog(this), new OutputData(qty, date));
        }
    }
